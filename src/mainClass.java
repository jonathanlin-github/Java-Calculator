    @Override
    public void keyPressed(KeyEvent arg0) {
        char keyPressed = arg0.getKeyChar();

        if (keyPressed == 'a') {
            System.out.println(simpleLastNum);
            System.out.println(simpleCurrentNum);
        }
        if (Character.isDigit(keyPressed)) {
            updateNumber(Character.getNumericValue(keyPressed));
        }
        if (keyPressed == '+') {
            buttonAddMouseClicked(null);
        }
        if (keyPressed == '-') {
            buttonSubtractMouseClicked(null);
        }
        if (keyPressed == '*' || keyPressed == 'x') {
            buttonMultiplyMouseClicked(null);
        }
        if (keyPressed == '/') {
            buttonDivideMouseClicked(null);
        }
        if (keyPressed == '=' || arg0.getKeyCode() == KeyEvent.VK_ENTER) {
            buttonEqualsMouseClicked(null);
        }
    }

