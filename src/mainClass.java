    private void buttonEqualsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonEqualsMouseClicked
        // TODO add your handling code here:
        
        if (justOperated == true) {
            lastNumDisplay.setText(simpleLastNum +" "+ simpleOperation + " "+justOperatedNum +" = ");
            simpleCurrentNum = justOperatedNum;
        }
        else {
            lastNumDisplay.setText(simpleLastNum +" "+ simpleOperation + " "+simpleCurrentNum +" = ");
        }
        if (simpleOperation.equals("+")) {
            if (justOperated == false) {
                justOperatedNum = simpleCurrentNum;
                
            }
            
            simpleCurrentNum = simpleLastNum + simpleCurrentNum;
            currentNumDisplay.setText(String.valueOf(simpleCurrentNum));
            simpleLastNum = simpleCurrentNum;
            
            justOperated = true;
        }
        if (simpleOperation.equals("-")) {
            if (justOperated == false) {
                justOperatedNum = simpleCurrentNum;
                
            }
            
            simpleCurrentNum = simpleLastNum - simpleCurrentNum;
            currentNumDisplay.setText(String.valueOf(simpleCurrentNum));
            simpleLastNum = simpleCurrentNum;
            
            justOperated = true;
        }
        if (simpleOperation.equals("*")) {
            if (justOperated == false) {
                justOperatedNum = simpleCurrentNum;
                
            }
            
            simpleCurrentNum = simpleLastNum * simpleCurrentNum;
            currentNumDisplay.setText(String.valueOf(simpleCurrentNum));
            simpleLastNum = simpleCurrentNum;
            
            justOperated = true;
        }
        if (simpleOperation.equals("/")) {
            if (justOperated == false) {
                justOperatedNum = simpleCurrentNum;
                
            }
            
            simpleCurrentNum = simpleLastNum / simpleCurrentNum;
            currentNumDisplay.setText(String.valueOf(simpleCurrentNum));
            simpleLastNum = simpleCurrentNum;
            
            justOperated = true;
        }
    }//GEN-LAST:event_buttonEqualsMouseClicked

