    public void updateNumber(int number) {
        if (justOperated == true) {
            simpleCurrentNum = 0;
        }
        justOperated = false;
        if (currentNumDisplay.getText().length() <= 16) {
            if (decimalMode == false) {
                simpleCurrentNum = simpleCurrentNum * 10;
                simpleCurrentNum = simpleCurrentNum + number;
                currentNumDisplay.setText(String.valueOf(simpleCurrentNum));
            }
            if (decimalMode == true) {
                String temp = String.valueOf(simpleCurrentNum);
                temp = temp + String.valueOf(number);
                simpleCurrentNum = Long.valueOf(temp);
                currentNumDisplay.setText(String.valueOf(simpleCurrentNum));
            }
        }


        
    }
    
    public void doOperation(String op) {
        simpleOperation = op;
        simpleLastNum = simpleCurrentNum;
        lastNumDisplay.setText(String.valueOf(simpleLastNum) + " " + simpleOperation + " ");
        simpleCurrentNum = 0;
    }

